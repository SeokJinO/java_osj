package day22;

public class Day22_assignment_1 {
	int kor,eng,math;

	public Day22_assignment_1(int kor, int eng, int math) {
		this.kor = kor;
		this.eng = eng;
		this.math = math;
	}

	public int getKor() {
		return kor;
	}

	public void setKor(int kor) {
		this.kor = kor;
	}

	public int getEng() {
		return eng;
	}

	public void setEng(int eng) {
		this.eng = eng;
	}

	public int getMath() {
		return math;
	}

	public void setMath(int math) {
		this.math = math;
	}

	@Override
	public String toString() {
		return " 국어 : " + kor + " 영어 : " + eng + " 수학 : " + math;
	}
	
	
}
